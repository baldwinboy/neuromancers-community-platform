class EmojiPickerWidget(forms.TextInput):
    """
    A text input with an emoji picker popup for Wagtail admin.
    Uses vanilla JS - no React required.
    """

    template_name = "wagtailadmin/widgets/emoji_picker.html"

    def __init__(self, attrs=None):
        default_attrs = {"class": "emoji-picker-input", "readonly": "readonly"}
        if attrs:
            default_attrs.update(attrs)
        super().__init__(attrs=default_attrs)

    def get_context(self, name, value, attrs):
        context = super().get_context(name, value, attrs)
        context["emoji_categories"] = get_categorized_emojis()
        context["emoji_categories_json"] = mark_safe(
            json.dumps(get_categorized_emojis())
        )
        return context

    class Media:
        css = {"all": ("css/emoji_picker.css",)}
        js = ("js/emoji_picker.js",)
